 <b><span style="color: #ff0000;">Технологии комплектных трансформаторных подстанций (КТП):</span></b>
</p>
<p>
	 Разнообразие видов:
</p>
<p>
	 - Облегченные
</p>
<p>
	 - Киосковые
</p>
<p>
	 - Мачтовые
</p>
<p>
	 - Столбовые
</p>
<p>
	 Простота в обслуживании.
</p>
<p>
	 Может поставляться отдельными блоками или в собранном виде.
</p>
<p>
 <b><span style="color: #ff0000;">Технологии:</span></b>
</p>
<p>
	 - Конструкции подстанций изготавливаются из листового металла толщиной 1,5-2 мм, обеспечивающей неоднократные погрузо-разгрузочные и монтажные операции без разрушения подстанции, а также в утепленном варианте из сэндвич-панелей по желанию заказчика.
</p>
<p>
	 - Подстанции изготовлены в соответствии с требованиями ГОСТ 14695-80, правилами устройства электроустановок (ПУЭ), ТУ и имеют сертификаты соответствия. Условия эксплуатации подстанций КТП: от -45°С до +50°С.
</p>
<p>
	 - Имеются распашные двустворчатые двери, обеспечивающие доступ к трансформатору. По желанию заказчика двери отсеков выполняются в антивандальном исполнении. Чтобы не допустить самопроизвольного закрытия дверей под воздействием ветра, на дверях реализуются фиксаторы.
</p>
<p>
	 - Аппаратура может быть закрыта защитной панелью.
</p>
<p>
	 - Имеются механические блокировки по ГОСТ 12.2.007.4-75: замок Гинодмана и система сетчатого заграждения, благодаря которым работа обслуживающего персонала становится безопасной.
</p>
<p>
	 - Возможность изготовления металлоконструкций из оцинкованного железа с нанесением порошковой краски, что увеличивает гарантию от сквозной коррозии металлоконструкций до 20 лет.
</p>
<p>
	 - Возможность изготовления КТП с применением заклепочно-болтовой конструкции металлооболочки, что в свою очередь увеличивает стойкость трансформаторной подстанции к воздействию окружающей среды.
</p>
<p>
	 - Возможность изготовления трансформаторной подстанции с использованием комплектующих ведущих мировых производителей, таких как «SchneiderElectric», «Legrand», «IEK», а также ведущих отечественных производителей коммутационной аппаратуры.
</p>
<p>
	 - Использование в составе КТП трансформаторов типа ТМГ сводит к минимуму обслуживание подстанции, так как трансформаторы данного типа на всем сроке эксплуатации не нуждаются в обслуживании.
</p>
<p>
	 - Возможность применения кабельного ввода.
</p>
<p>
	 - Уменьшенные габаритные размеры, позволяющие находить оптимальные решения в соответствии с требованием Заказчика.
</p>
<p>
	 - Любые типы коммутационной аппаратуры.
</p>
<p>
	 - Особая механическая прочность.
</p>
<p>
	 - Пыле и влагозащитная конструкция оболочки.
</p>
<p>
	 - Токоведущие части закрыты защитными панелями.
</p>
<p>
	 - По желанию заказчика применяются регулируемые жалюзи, эффективные в борьбе с конденсацией влаги, а также позволяющие избежать тепловых перегрузок силового оборудования КТП.
</p>
<p>
	 - Применяются устройства защиты от перенапряжений на стороне 6(10) кВ и 0,4 кВ.
</p>
<p>
	 - Возможно осуществление интеграции КТП в систему АСУ ТП.
</p>
<p>
	 - Контроль качества осуществляется на каждом этапе производства трансформаторной подстанции. Все трансформаторы проходят приемо-сдаточные испытания на собственной испытательной станции.
</p>
<p>
	 - Конструкция шкафов обеспечивает простоту, удобство и безопасность доступа для проверки, регулировки, технического обслуживания блоков и аппаратов.
</p>
<p>
	 - По желанию заказчика все силовые шины выполнены из высококачественной меди, что обеспечивает надежную и длительную работу КТП в тяжелых условиях эксплуатации.
