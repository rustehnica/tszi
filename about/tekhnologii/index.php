 <b><span style="color: #ff0000;">Технологии силовых масляных трансформаторов:</span></b>
</p>
<p>
	 - Для окраски наружной поверхности бака используются атмосферостойкие светло-серые порошковые краски.
</p>
<p>
	 - Все уплотнения трансформатора выполняются из маслостойкой резины.
</p>
<p>
	 - Внутренний объем бака трансформатора не связан с внешней средой, что значительно улучшает условия работы масла, исключает его увлажнение, окисление и шламообразование.&nbsp;
</p>
<p>
	 - Для того, чтобы исключить повышение давления внутри бака выше допустимого при температурном расширении масла, возникающее в результате его нагрева, в верхней части бака предусмотрен компенсационный зазор.
</p>
<p>
	 - Высокая устойчивость оболочек изделий к деформациям при транспортировках обеспечивается за счет стенок, выполненных из стального листа толщиной от 2,5 мм до 3,5 мм с ребрами жесткости.
</p>
<p>
	 - Для исключения недопустимого превышения давления, возникающего в результате перегрузок, используется предохранительный клапан.
</p>
<p>
	 - Магнитная система&nbsp;изготавливается из холоднокатаной электротехнической стали.
</p>
<p>
	 - Обмотки многослойные цилиндрические, выполнены из провода круглого или прямоугольного сечения с эмалевой или стеклополиэфирной изоляцией. Обмотки изготавливаются из алюминиевых обмоточных проводов.
</p>
<p>
	 - Межслойная изоляция выполнена из кабельной бумаги. Нижние и верхние ярмовые балки изготавливаются из гнутых профилей специальной конструкции, обеспечивающей высокую механическую прочность. Отводы обмотки ВН выполнены из провода круглого или прямоугольного сечения, отводы обмотки НН - из прямоугольной шины.
</p>
<p>
	 - Для исключения недопустимых перегрузок трансформаторов при несимметричных нагрузках, нулевой и фазные токоведущие части низкого напряжения выпускаемых трансформаторов имеют одинаковое сечение.
</p>
<p>
	 - Переключатель ответвлений обмоток (ПБВ) реечный типа ПТР-6-10/63 или ПТР-6-10/150 обеспечивает регулирование напряжения обмотки ВН четырьмя ступенями по 2,5% при отключенном от сети трансформаторе.
</p>
<p>
	 - Уровень масла в трансформаторах контролируется визуально по указателю уровня масла, который расположен на стенке бака.
